@pytest.mark.parametrize("flat_rate,aum,annual_benefit", [
    (0.03, 2_000_000.0, 60_000.0),   # no disinvestment needed
    (0.04, 1_800_000.0, 80_000.0),   # light disinvestment
    (0.037, 2_222_430.0, 100_000.0), # tie-break scenario (reproduces 2026-03 bug)
])
def test_alm_60month_parity_python_vs_excel(flat_rate, aum, annual_benefit):
    """Full 60-month step-by-step parity: Python _alm_disinvest vs Excel formula sim.

    Asserts:
    - Cash EOM matches at every month (|diff| <= TOL_DOLLAR)
    - Total asset MV matches at every month (|diff| <= TOL_DOLLAR)
    - Month 60 AUM matches (|diff| <= TOL_DOLLAR)
    """
    yc = _flat_yc(flat_rate)

    # Build synthetic constant CFs (simplified: flat monthly payment)
    monthly_cf = annual_benefit / 12.0
    cfs = np.full(60, monthly_cf)

    py_cash, py_face, py_mv = _run_full_sim(aum, cfs, yc, use_excel_disinvest=False)
    xl_cash, xl_face, xl_mv = _run_full_sim(aum, cfs, yc, use_excel_disinvest=True)

    for m in range(60):
        np.testing.assert_allclose(
            py_cash[m], xl_cash[m], atol=TOL_DOLLAR,
            err_msg=f"Cash mismatch at month {m+1}: Python={py_cash[m]:.6f}, Excel={xl_cash[m]:.6f}",
        )
        np.testing.assert_allclose(
            py_mv[m], xl_mv[m], atol=TOL_DOLLAR,
            err_msg=f"Total MV mismatch at month {m+1}: Python={py_mv[m]:.6f}, Excel={xl_mv[m]:.6f}",
        )
        np.testing.assert_allclose(
            py_face[m], xl_face[m], atol=TOL_DOLLAR,
            err_msg=f"Face array mismatch at month {m+1}",
        )


@pytest.mark.regression
def test_alm_tie_break_scenario_zero_discrepancy():
    """Regression: the exact scenario that produced a 24.00 surplus discrepancy.

    The tie-break at month 35 (1Y and 3Y both nearing maturity simultaneously)
    must yield identical disinvestment in Python and Excel, producing zero
    discrepancy in total AUM at month 60.

    Fixed in 2026-03:
    - alm_excel_ladder.py: < 1E-9 → < 5E-10 threshold
    - spia_projection.py:  added np.arange(n)*1e-9 epsilon to argsort
    """
    flat_rate = 0.037
    aum = 2_222_430.0
    monthly_cf = 100_000.0 / 12.0
    cfs = np.full(60, monthly_cf)
    yc = _flat_yc(flat_rate)

    _, _, py_mv = _run_full_sim(aum, cfs, yc, use_excel_disinvest=False)
    _, _, xl_mv = _run_full_sim(aum, cfs, yc, use_excel_disinvest=True)

    discrepancy = abs(py_mv[59] - xl_mv[59])
    assert discrepancy < TOL_DOLLAR, (
        f"Surplus discrepancy at month 60: {discrepancy:.6f}. "
        f"Python AUM={py_mv[59]:.4f}, Excel AUM={xl_mv[59]:.4f}. "
        "This may indicate the epsilon tie-breaking fix was reverted."
    )
