def _flat_yc(rate: float) -> sp.YieldCurve:
    return sp.YieldCurve.from_flat_rate(rate)


def _df_rem(yc: sp.YieldCurve, t_arr: np.ndarray, spread: float = 0.0) -> np.ndarray:
    t = np.maximum(np.asarray(t_arr, dtype=float), 0.0)
    out = np.ones_like(t, dtype=float)
    mask = t > 1e-15
    if np.any(mask):
        out[mask] = yc.discount_factors(t[mask], spread=spread)
    return out


def _build_standard_buckets():
    """5-bucket allocation: 5% cash, 19% 1Y, 20% 3Y, 22% 5Y, 22% 10Y, 12% 20Y."""
    buckets = [
        sp.ALMBucketSpec(name="Cash", tenor_years=0),
        sp.ALMBucketSpec(name="1Y", tenor_years=1),
        sp.ALMBucketSpec(name="3Y", tenor_years=3),
        sp.ALMBucketSpec(name="5Y", tenor_years=5),
        sp.ALMBucketSpec(name="10Y", tenor_years=10),
        sp.ALMBucketSpec(name="20Y", tenor_years=20),
    ]
    weights = [0.05, 0.19, 0.20, 0.22, 0.22, 0.12]
    alloc = sp.ALMAllocationSpec(buckets=buckets, weights=weights)
    asm = sp.ALMAssumptions(
        allocation=alloc,
        rebalance_band=0.0,
        rebalance_frequency_months=12,
        reinvest_rule="pro_rata",
        disinvest_rule="shortest_first",
        rebalance_policy="full_target",
        liquidity_near_liquid_years=1.0,
    )
    return asm, np.array(weights), buckets


def _init_portfolio(aum: float, w: np.ndarray, buckets: list, yc: sp.YieldCurve, spread: float = 0.0):
    n_b = len(buckets) - 1
    nominal_tenors = np.array([float(b.tenor_years) for b in buckets[1:]])
    faces = np.zeros(n_b)
    t_rem = nominal_tenors.copy()
    for k in range(n_b):
        mv_t = w[k + 1] * aum
        d0 = float(yc.discount_factors(np.array([nominal_tenors[k]]), spread=spread)[0])
        faces[k] = mv_t / d0
    cash = float(w[0] * aum)
    return cash, faces, t_rem, nominal_tenors


def _run_full_sim(
    aum: float,
    cfs: np.ndarray,
    yc: sp.YieldCurve,
    use_excel_disinvest: bool = False,
    spread: float = 0.0,
) -> tuple[list, list, list]:
    """Run 60-month ALM step-by-step. Returns (cash_eom, face_eom, total_mv_eom) per month."""
    asm, w, buckets = _build_standard_buckets()
    cash, faces, t_rem, nominal_tenors = _init_portfolio(aum, w, buckets, yc, spread)
    dt = 1.0 / 12.0
    n_b = len(buckets) - 1

    cash_hist, face_hist, mv_hist = [], [], []

    for m in range(60):
        t_rem = np.maximum(t_rem - dt, 0.0)
        matured = (faces > 1e-15) & (t_rem <= 1e-14)
        for k in np.flatnonzero(matured):
            cash += float(faces[k])
            faces[k] = 0.0
        t_rem = np.where(faces <= 1e-15, 0.0, t_rem)
        df = _df_rem(yc, t_rem, spread)
        mv = faces * df

        if np.any(matured):
            cash, faces = sp._alm_micro_reinvest_pro_rata(
                cash=cash, faces=faces, t_rem=t_rem, w=w,
                yield_curve=yc, spread=spread, nominal_tenors=nominal_tenors,
            )
            df = _df_rem(yc, t_rem, spread)
            mv = faces * df

        cash -= float(cfs[m])
        need = max(0.0, -cash)
        if need > 1e-9:
            if use_excel_disinvest:
                cash, faces = excel_disinvest_shortest_first(cash, faces, t_rem, df, need)
            else:
                cash, faces = sp._alm_disinvest(
                    cash=cash, faces=faces, t_rem=t_rem,
                    yield_curve=yc, spread=spread, need=need,
                    rule="shortest_first",
                )
            df = _df_rem(yc, t_rem, spread)
            mv = faces * df

        cash_hist.append(float(cash))
        face_hist.append(faces.copy())
        mv_hist.append(float(cash + np.sum(mv)))

    return cash_hist, face_hist, mv_hist


